public class LCOF42 {
    public static void main(String[] args) {
        System.out.println(maxSubArray(new int[] {-2,1,-3,4,-1,2,1,-5,4}));
    }

    /**
     * 一维动态规划，计算第n位数位最后一个数字的连续子数组的最大和
     * 如果0-(n-1)的最大和max是正数，则0-n的最大和是num[n] + max；否则就是num[n];
     * 再筛选出上面得到所有最大和中的最大值
     * @param nums
     * @return
     */
    public static int maxSubArray(int[] nums) {
        int max = nums[0];
        for (int i = 1; i < nums.length; i++) {
            if (nums[i - 1] > 0) {
                nums[i] += nums[i - 1];
            }
            max = Math.max(max, nums[i]);
        }
        return max;
    }
}
